package com.tourapp.adapter;

public class Advisor {
    String id;
    String name;
    String contact;
    String hired_status;
    String img;

    public Advisor() {
    }

    public Advisor(String id, String name, String contact, String hired_status, String img) {
        this.id = id;
        this.name = name;
        this.contact = contact;
        this.hired_status = hired_status;
        this.img = img;
    }




}

